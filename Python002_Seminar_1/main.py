'''
5. Напишите программу, которая принимает на вход число и проверяет, кратно ли оно 5 и 10 или 15, но не 30.
'''


def task5():
    num = int(input('Введите число: '))
    if ((num % 5 == 0 and num % 10 == 0) or num % 15 == 0) and num % 30 != 0:
        print(f'число {num} кратно 5 и 10 или 15, но не кратно 30')
    else:
        print(f'число {num} не кратно 5 и 10 или 15, но не 30')


'''
6. Программа получает на вход число.
   Необходимо вывести список простых множителей этого числа.
'''


def task6():
    a = int(input("Введите число: "))
    result = []
    temp_a = a

    for i in range(2, a):
        while temp_a % i == 0:
            result.append(i)
            temp_a = temp_a // i

    print(result)


